package top.sillyfan.common.date;

public interface DateFormat {
	String YYYY_MM_DD = "yyyy-MM-dd";

	String YYYYMMDD = "yyyyMMdd";

	String YYYY_MM_DD_HH_MM_SS = "yyyy-MM-dd HH:mm:ss";

	String YYYY_MM_DD_THH_MM_SS = "yyyy-MM-dd'T'HH:mm:ss";

	String YYYY_MM_DD_HH_MM = "yyyy-MM-dd HH:mm";

	String HHMMSS = "HHmmss";

	String MMSS = "mmss";

	String YYYY_MM_DD_FORWARD_SLASH = "yyyy/MM/dd";

	String YYYYMMDDHH = "yyyyMMddHH";

	String YYYY_MM_DD_HH_MM_SS_SSS_Z = "yyyy-MM-dd'T'HH:mm:ss.sssZ";

}
